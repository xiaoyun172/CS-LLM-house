#!/usr/bin/env python3
"""
测试文件上传功能
这是一个简单的Python脚本，用于测试移动端文件上传和内容解析功能
"""

def hello_world():
    """打印Hello World"""
    print("Hello, World!")
    print("这是一个测试Python文件")
    print("用于验证移动端文件上传功能")

def calculate_sum(a, b):
    """计算两个数的和"""
    return a + b

def main():
    """主函数"""
    hello_world()
    result = calculate_sum(10, 20)
    print(f"10 + 20 = {result}")

if __name__ == "__main__":
    main()
